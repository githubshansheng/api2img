const categories: PromptTemplateCategory[] = categorySeeds.map((category) => ({
  id: category.id,
  label: category.label,
  description: category.description,
  templates: category.seeds.map((seed, index) => ({
    id: `${category.id}-${String(index + 1).padStart(2, "0")}`,
    categoryId: category.id,
    title: seed.title,
    prompt: seed.prompt,
    tags: seed.tags
  }))
}));

export function getPromptTemplateCategories() {
  return categories;
}

export function getPromptTemplateById(templateId: string) {
  return categories.flatMap((category) => category.templates).find((template) => template.id === templateId);
}

export function getPromptTemplateCount() {
  return categories.reduce((total, category) => total + category.templates.length, 0);
}

export function searchPromptTemplates(query: string, categoryId?: string) {
  const keyword = query.trim().toLowerCase();
  const scoped = categoryId
    ? categories.find((category) => category.id === categoryId)?.templates ?? []
    : categories.flatMap((category) => category.templates);

  if (!keyword) {
    return scoped;
  }

  return scoped.filter((template) =>
    [template.title, template.prompt, ...template.tags].some((value) => value.toLowerCase().includes(keyword))
  );
}
